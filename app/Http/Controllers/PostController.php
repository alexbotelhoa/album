    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $post = Post::find($id);
        if (isset($post)) {
            $file = $post->arquivo;
            Storage::disk('public')->delete($file);
            $post->delete();
        }
        return redirect('/');
    }
